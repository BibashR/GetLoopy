public class PartA {
    public static void main(String[] args) {
        int cnt;

        // Task 1
        System.out.print("Task1: ");
        for (cnt= 0; cnt <= 30; cnt++) {
            System.out.print(cnt + " ");
        }
        System.out.println(" ");

        // Task  2
        System.out.print("Task2: ");
        for (cnt= 30; cnt >= 0; cnt--) {
            System.out.print(cnt + " ");
        }
        System.out.println("");

        // Task  3
        System.out.print("Task3: ");
        for (cnt= 0; cnt <= 18; cnt+=3) {
            System.out.print(cnt + " ");
        }
        System.out.println("");

        // Task  4
        System.out.print("Task4: ");
        for (cnt= 10; cnt >= 0; cnt-=2) {
            System.out.print(cnt + " ");
        }
        System.out.println("");
        }
    }